# FORMULATION REPORT
# ═══════════════════════════════════════════════════════════════

def generate_formulation_report(result, filepath: str) -> str:
    """Generate formulation simulation HTML report."""
    stats = result.summary()

    # ── Cost Distribution Chart ──────────────────────────────
    fig, ax = plt.subplots(figsize=(10, 5))
    ax.hist(result.total_costs, bins=80, color="#3b82f6", alpha=0.8, edgecolor="white")
    ax.axvline(stats["mean"], color="#ef4444", linewidth=2, linestyle="--", label=f"Mean: ₹{stats['mean']:.2f}")
    ax.axvline(stats["p5"], color="#f59e0b", linewidth=1.5, linestyle=":", label=f"P5: ₹{stats['p5']:.2f}")
    ax.axvline(stats["p95"], color="#f59e0b", linewidth=1.5, linestyle=":", label=f"P95: ₹{stats['p95']:.2f}")
    ax.set_xlabel("Total Batch Cost (₹)", fontsize=12)
    ax.set_ylabel("Frequency", fontsize=12)
    ax.set_title("Total Formulation Cost Distribution", fontsize=14, fontweight="bold")
    ax.legend(fontsize=10)
    ax.grid(axis="y", alpha=0.3)
    cost_chart = _fig_to_base64(fig)

    # ── Ingredient Cost Contribution Chart ───────────────────
    fig, ax = plt.subplots(figsize=(10, 5))
    ing_names = [i.name for i in result.ingredients]
    ing_means = [np.mean(result.ingredient_costs[n]) for n in ing_names]
    ing_stds = [np.std(result.ingredient_costs[n]) for n in ing_names]
    colors = plt.cm.Set2(np.linspace(0, 1, len(ing_names)))
    bars = ax.barh(ing_names, ing_means, xerr=ing_stds, color=colors,
                   edgecolor="white", capsize=4)
    ax.set_xlabel("Cost Contribution (₹)", fontsize=12)
    ax.set_title("Ingredient Cost Contributions (Mean ± Std)", fontsize=14, fontweight="bold")
    ax.grid(axis="x", alpha=0.3)
    ingredient_chart = _fig_to_base64(fig)

    # ── Pie Chart ────────────────────────────────────────────
    fig, ax = plt.subplots(figsize=(7, 7))
    ax.pie(ing_means, labels=ing_names, colors=colors, autopct="%1.1f%%",
           startangle=90, pctdistance=0.85)
    ax.set_title("Cost Share by Ingredient", fontsize=14, fontweight="bold")
    pie_chart = _fig_to_base64(fig)

    # ── Build HTML ───────────────────────────────────────────
    cost_table = result.cost_breakdown().to_html(index=False, classes="")
    nutrition_html = ""
    if not result.nutrition_summary().empty:
        nutrition_html = f"""
        <div class="card">
            <h2>🥗 Nutritional Profile (per 100g of product)</h2>
            {result.nutrition_summary().to_html(index=False, classes="")}
        </div>"""

    content = f"""
    <div class="card">
        <h2>📋 Simulation Summary</h2>
        <div class="metric-grid">
            <div class="metric">
                <div class="label">Mean Batch Cost</div>
                <div class="value">₹{stats['mean']:.2f}</div>
                <div class="detail">per {result.ingredients[0].unit.split('/')[0] if result.ingredients else ''} batch</div>
            </div>
            <div class="metric green">
                <div class="label">Best Case (P5)</div>
                <div class="value">₹{stats['p5']:.2f}</div>
            </div>
            <div class="metric red">
                <div class="label">Worst Case (P95)</div>
                <div class="value">₹{stats['p95']:.2f}</div>
            </div>
            <div class="metric amber">
                <div class="label">Cost Variability (CV)</div>
                <div class="value">{stats['cv']:.1f}%</div>
            </div>
            <div class="metric">
                <div class="label">Simulations</div>
                <div class="value">{result.n_simulations:,}</div>
            </div>
            <div class="metric">
                <div class="label">Ingredients</div>
                <div class="value">{len(result.ingredients)}</div>
            </div>
        </div>
    </div>

    <div class="card">
        <h2>📊 Cost Distribution</h2>
        <img class="chart" src="data:image/png;base64,{cost_chart}" alt="Cost Distribution">
    </div>

    <div class="card">
        <h2>🧩 Ingredient Cost Breakdown</h2>
        {cost_table}
        <div class="two-col">
            <img class="chart" src="data:image/png;base64,{ingredient_chart}" alt="Ingredient Costs">
            <img class="chart" src="data:image/png;base64,{pie_chart}" alt="Cost Pie">
        </div>
    </div>

    {nutrition_html}
    """

    html = _html_wrapper("Formulation Cost Simulation", content)
    with open(filepath, "w") as f:
        f.write(html)
    print(f"📄 Report saved: {filepath}")
    return filepath


