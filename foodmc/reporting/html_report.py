# SHELF LIFE REPORT
# ═══════════════════════════════════════════════════════════════

def generate_shelflife_report(result, filepath: str) -> str:
    """Generate shelf life simulation HTML report."""
    stats = result.summary()
    params = result.parameters
    rec_95 = result.recommended_shelf_life(0.95)
    rec_99 = result.recommended_shelf_life(0.99)

    # ── Shelf Life Distribution ──────────────────────────────
    fig, ax = plt.subplots(figsize=(10, 5))
    ax.hist(result.shelf_lives, bins=80, color="#22c55e", alpha=0.8, edgecolor="white")
    ax.axvline(stats["mean"], color="#ef4444", linewidth=2, linestyle="--",
               label=f"Mean: {stats['mean']:.0f} days")
    ax.axvline(rec_95, color="#f59e0b", linewidth=2, linestyle="--",
               label=f"95% Safe: {rec_95:.0f} days")
    ax.set_xlabel("Shelf Life (days)", fontsize=12)
    ax.set_ylabel("Frequency", fontsize=12)
    ax.set_title("Shelf Life Distribution", fontsize=14, fontweight="bold")
    ax.legend(fontsize=10)
    ax.grid(axis="y", alpha=0.3)
    sl_chart = _fig_to_base64(fig)

    # ── Degradation Curve ────────────────────────────────────
    curves = result.degradation_curves
    max_time = min(int(stats["p95"] * 1.5), len(curves["time"]))
    fig, ax = plt.subplots(figsize=(10, 5))
    t = curves["time"][:max_time]
    ax.fill_between(t, curves["pessimistic"][:max_time], curves["optimistic"][:max_time],
                    alpha=0.2, color="#3b82f6", label="90% Confidence Band")
    ax.plot(t, curves["mean"][:max_time], color="#3b82f6", linewidth=2, label="Mean Degradation")
    ax.axhline(params["threshold"], color="#ef4444", linewidth=2, linestyle="--",
               label=f"Threshold: {params['threshold']}")
    ax.set_xlabel("Time (days)", fontsize=12)
    ax.set_ylabel(params["quality_attribute"], fontsize=12)
    ax.set_title("Quality Degradation Over Time", fontsize=14, fontweight="bold")
    ax.legend(fontsize=10)
    ax.grid(alpha=0.3)
    deg_chart = _fig_to_base64(fig)

    # ── Survival Curve ───────────────────────────────────────
    fig, ax = plt.subplots(figsize=(10, 5))
    sorted_sl = np.sort(result.shelf_lives)
    survival = 1 - np.arange(1, len(sorted_sl) + 1) / len(sorted_sl)
    ax.plot(sorted_sl, survival * 100, color="#8b5cf6", linewidth=2)
    ax.axhline(5, color="#ef4444", linewidth=1, linestyle=":", alpha=0.7, label="5% failure")
    ax.axhline(1, color="#ef4444", linewidth=1, linestyle="--", alpha=0.7, label="1% failure")
    ax.set_xlabel("Days", fontsize=12)
    ax.set_ylabel("Survival Probability (%)", fontsize=12)
    ax.set_title("Product Survival Curve", fontsize=14, fontweight="bold")
    ax.legend(fontsize=10)
    ax.grid(alpha=0.3)
    surv_chart = _fig_to_base64(fig)

    content = f"""
    <div class="card">
        <h2>📋 Shelf Life Summary</h2>
        <div class="metric-grid">
            <div class="metric green">
                <div class="label">Recommended Shelf Life (95%)</div>
                <div class="value">{rec_95:.0f} days</div>
                <div class="detail">95% of batches safe by this date</div>
            </div>
            <div class="metric">
                <div class="label">Mean Shelf Life</div>
                <div class="value">{stats['mean']:.0f} days</div>
            </div>
            <div class="metric amber">
                <div class="label">Conservative (99%)</div>
                <div class="value">{rec_99:.0f} days</div>
            </div>
            <div class="metric">
                <div class="label">Std Deviation</div>
                <div class="value">{stats['std']:.1f} days</div>
            </div>
            <div class="metric">
                <div class="label">Storage Temp</div>
                <div class="value">{params['temp_mean']:.0f}°C ± {params['temp_std']:.0f}°C</div>
            </div>
            <div class="metric">
                <div class="label">Simulations</div>
                <div class="value">{result.n_simulations:,}</div>
            </div>
        </div>
    </div>

    <div class="card">
        <h2>📊 Shelf Life Distribution</h2>
        <img class="chart" src="data:image/png;base64,{sl_chart}" alt="Shelf Life Distribution">
    </div>

    <div class="card">
        <h2>📉 Quality Degradation Curve</h2>
        <img class="chart" src="data:image/png;base64,{deg_chart}" alt="Degradation Curve">
    </div>

    <div class="card">
        <h2>📈 Product Survival Curve</h2>
        <img class="chart" src="data:image/png;base64,{surv_chart}" alt="Survival Curve">
    </div>

    <div class="card">
        <h2>⚙️ Simulation Parameters</h2>
        <table>
            <tr><th>Parameter</th><th>Value</th></tr>
            <tr><td>Activation Energy (Ea)</td><td>{params['ea_mean']} ± {params['ea_std']} kJ/mol</td></tr>
            <tr><td>Storage Temperature</td><td>{params['temp_mean']} ± {params['temp_std']} °C</td></tr>
            <tr><td>Initial Quality</td><td>{params['initial_quality']} ± {params['quality_std']}</td></tr>
            <tr><td>Quality Threshold</td><td>{params['threshold']}</td></tr>
            <tr><td>Reaction Order</td><td>{params['reaction_order']}</td></tr>
            <tr><td>Reference Rate</td><td>{params['ref_rate']} ± {params['ref_rate_std']} /day</td></tr>
        </table>
    </div>
    """

    html = _html_wrapper("Shelf Life Prediction", content)
    with open(filepath, "w") as f:
        f.write(html)
    print(f"📄 Report saved: {filepath}")
    return filepath


