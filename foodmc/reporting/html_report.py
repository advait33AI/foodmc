# ═══════════════════════════════════════════════════════════════

def generate_quality_report(result, filepath: str) -> str:
    """Generate quality control simulation HTML report."""

    charts_html = ""
    for param in result.parameters:
        name = param["name"]
        sims = result.simulations[name]
        cpk = result.cpk_values[name]

        # ── Process Histogram with Spec Limits ───────────────
        fig, axes = plt.subplots(1, 2, figsize=(14, 5))

        ax = axes[0]
        ax.hist(sims, bins=80, color="#3b82f6", alpha=0.8, edgecolor="white", density=True)
        ax.axvline(param["lsl"], color="#ef4444", linewidth=2, linestyle="--", label=f"LSL: {param['lsl']}")
        ax.axvline(param["usl"], color="#ef4444", linewidth=2, linestyle="--", label=f"USL: {param['usl']}")
        ax.axvline(param["target"], color="#22c55e", linewidth=2, linestyle="-", label=f"Target: {param['target']}")
        ax.set_title(f"{name} — Process Distribution", fontsize=13, fontweight="bold")
        ax.set_xlabel(f"{name} ({param['unit']})" if param["unit"] else name)
        ax.legend(fontsize=9)
        ax.grid(axis="y", alpha=0.3)

        ax = axes[1]
        ax.hist(cpk, bins=60, color="#8b5cf6", alpha=0.8, edgecolor="white")
        ax.axvline(1.0, color="#ef4444", linewidth=2, linestyle="--", label="Cpk = 1.0 (minimum)")
        ax.axvline(1.33, color="#f59e0b", linewidth=2, linestyle="--", label="Cpk = 1.33 (good)")
        ax.set_title(f"{name} — Cpk Distribution", fontsize=13, fontweight="bold")
        ax.set_xlabel("Cpk")
        ax.legend(fontsize=9)
        ax.grid(axis="y", alpha=0.3)

        plt.tight_layout()
        chart_b64 = _fig_to_base64(fig)

        prob_in_spec = result.probability_in_spec(name) * 100
        mean_cpk = np.mean(cpk)
        cpk_color = "green" if mean_cpk >= 1.33 else ("amber" if mean_cpk >= 1.0 else "red")

        charts_html += f"""
        <div class="card">
            <h2>📏 {name}</h2>
            <div class="metric-grid">
                <div class="metric {cpk_color}">
                    <div class="label">Mean Cpk</div>
                    <div class="value">{mean_cpk:.3f}</div>
                </div>
                <div class="metric {'green' if prob_in_spec >= 99 else 'red'}">
                    <div class="label">In-Spec Rate</div>
                    <div class="value">{prob_in_spec:.2f}%</div>
                </div>
                <div class="metric">
                    <div class="label">Process Mean</div>
                    <div class="value">{np.mean(sims):.3f} {param['unit']}</div>
                </div>
                <div class="metric">
                    <div class="label">Process Std</div>
                    <div class="value">{np.std(sims):.4f}</div>
                </div>
            </div>
            <img class="chart" src="data:image/png;base64,{chart_b64}" alt="{name} Charts">
        </div>
        """

    summary_table = result.summary().to_html(index=False, classes="")

    content = f"""
    <div class="card">
        <h2>📋 Quality Control Summary</h2>
        {summary_table}
    </div>
    {charts_html}
    """

    html = _html_wrapper("Quality Control Simulation", content)
    with open(filepath, "w") as f:
        f.write(html)
    print(f"📄 Report saved: {filepath}")
    return filepath


